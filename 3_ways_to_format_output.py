#print() — 3 Ways to Format Output
#Way 1 — Using commas:
name = "John"
age = 25
print("Name:", name, "Age:", age)
#Way 2 — Using + (concatenation, strings only):
name = "John"
print("Hello " + name + "!")
#Way 3 — f-strings (Best and most modern way!):
name = "John"
age = 25
print(f"My name is {name} and I am {age} years old.")