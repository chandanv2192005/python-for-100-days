# Functions with Parameters
def greet(name):
    print(f"Hello, {name}! Welcome to Python!")

greet("John") # Hello, John! Welcome to Python!
greet("Sarah") # Hello, Sarah! Welcome to Python!

# Multiple parameters
def add(num1, num2):
    print(f"{num1} + {num2} = {num1 + num2}")

add(5, 3) # 5 + 3 = 8
add(10, 20) # 10 + 20 = 30
